package com.ycdc.dingding.model.entity;

/**
 * @name: LockPasswordPermission.java
 * @Description: 
 * @author duanyongrui
 * @since: 2017年5月22日
 */
public class LockPasswordPermission {
	/**
	 * 密码有效期的开始时间时间戳，单位 S
	 */
	private int begin;
	/**
	 * 密码有效期的结束时间时间戳，单位 S
	 */
	private int end;
	/**
	 * 密码权限类型:
	 * 1:永久密码
	 * 2:时间有效密码
	 * 注:saas 平台生成的密码均为类型 2;锁本地生成的密码都为类型 1
	 */
	private int status;
	public int getBegin() {
		return begin;
	}
	public void setBegin(int begin) {
		this.begin = begin;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}
