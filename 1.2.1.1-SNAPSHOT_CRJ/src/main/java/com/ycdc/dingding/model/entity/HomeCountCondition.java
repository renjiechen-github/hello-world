package com.ycdc.dingding.model.entity;

/**
 * @name: HomeCountCondition.java
 * @Description: 
 * @author duanyongrui
 * @since: 2017年5月22日
 */
public class HomeCountCondition {
	/**
	 * 公寓名称（支持模糊匹配）
	 */
	private String home_name;
	/**
	 * 小区名（支持模糊匹配）
	 */
	private String block;
	/**
	 * 坐落地址（支持模糊匹配）
	 */
	private String location;
	public String getHome_name() {
		return home_name;
	}
	public void setHome_name(String home_name) {
		this.home_name = home_name;
	}
	public String getBlock() {
		return block;
	}
	public void setBlock(String block) {
		this.block = block;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	/**
	 * 
	 */
	public HomeCountCondition() {
		super();
	}
	/**
	 * @param home_name
	 * @param block
	 * @param location
	 */
	public HomeCountCondition(String home_name, String block, String location) {
		super();
		this.home_name = home_name;
		this.block = block;
		this.location = location;
	}
	
}
