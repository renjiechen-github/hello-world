package com.ycdc.dingding.model.entity;

/**
 * @name: LockInfo.java
 * @Description: 
 * @author duanyongrui
 * @since: 2017年5月22日
 */
public class LockInfo extends HomeCenterInfo {
	/**
	 * 门锁的电量信息，-1 表示未知
	 */
	private int power;
	/**
	 * 最近一次电量更新时间戳，单位 S
	 */
	private int power_refreshtime;
	/**
	 * 门锁信号
	 */
	private int lqi;
	/**
	 * 门锁信号刷新
	 */
	private int lqi_refreshtime;
	/**
	 * 门锁所绑定的网关名称、描述
	 */
	private String center_description;
	/**
	 * 门锁所绑定的网关 uuid
	 */
	private String center_uuid;
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public int getPower_refreshtime() {
		return power_refreshtime;
	}
	public void setPower_refreshtime(int power_refreshtime) {
		this.power_refreshtime = power_refreshtime;
	}
	public int getLqi() {
		return lqi;
	}
	public void setLqi(int lqi) {
		this.lqi = lqi;
	}
	public int getLqi_refreshtime() {
		return lqi_refreshtime;
	}
	public void setLqi_refreshtime(int lqi_refreshtime) {
		this.lqi_refreshtime = lqi_refreshtime;
	}
	public String getCenter_description() {
		return center_description;
	}
	public void setCenter_description(String center_description) {
		this.center_description = center_description;
	}
	public String getCenter_uuid() {
		return center_uuid;
	}
	public void setCenter_uuid(String center_uuid) {
		this.center_uuid = center_uuid;
	}
}
