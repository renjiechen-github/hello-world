package com.ycdc.appserver.model.house;

import java.util.ArrayList;
import java.util.List;
import java.util.Map;

import com.ycdc.appserver.model.syscfg.Area;
import com.ycdc.appserver.model.syscfg.DictiItem;
import com.ycdc.appserver.model.user.User;

/**
 * 返回条件结果
 * @author suntf
 * @date 2016年12月1日
 */
public class ResultCondition
{
	/**
	 * 区域
	 */
	private List<Area> areas;
	
	/**
	 * 管家
	 */
	private List<User> users;
	
	private List<Map<String, Object>> categoryTypes = new ArrayList<>();
	
	/**
	 * 状态
	 */
	private List<DictiItem> stateList;
	
	/**
	 * 发布状态
	 */
	private List<DictiItem> publishList;

	public List<Map<String, Object>> getCategoryTypes()
	{
		return categoryTypes;
	}

	public void setCategoryTypes(List<Map<String, Object>> categoryTypes)
	{
		this.categoryTypes = categoryTypes;
	}

	/**
	 * @return the areas
	 */
	public List<Area> getAreas()
	{
		return areas;
	}

	/**
	 * @param areas the areas to set
	 */
	public void setAreas(List<Area> areas)
	{
		this.areas = areas;
	}

	/**
	 * @return the users
	 */
	public List<User> getUsers()
	{
		return users;
	}

	/**
	 * @param users the users to set
	 */
	public void setUsers(List<User> users)
	{
		this.users = users;
	}

	/**
	 * @return the stateList
	 */
	public List<DictiItem> getStateList()
	{
		return stateList;
	}

	/**
	 * @param stateList the stateList to set
	 */
	public void setStateList(List<DictiItem> stateList)
	{
		this.stateList = stateList;
	}

	/**
	 * @return the publishList
	 */
	public List<DictiItem> getPublishList()
	{
		return publishList;
	}

	/**
	 * @param publishList the publishList to set
	 */
	public void setPublishList(List<DictiItem> publishList)
	{
		this.publishList = publishList;
	}
}
