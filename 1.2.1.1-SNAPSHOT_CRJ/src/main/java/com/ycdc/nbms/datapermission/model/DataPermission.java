package com.ycdc.nbms.datapermission.model;

import java.io.Serializable;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

/**
 * 数据权限
 * 
 * @author sunchengming
 * @date 2017年9月20日 下午2:51:09
 * 
 */
public class DataPermission implements Serializable
{
	private static final long serialVersionUID = 3596006942879742138L;

	/**
	 * 主键
	 */
	private int id;

	/**
	 * 角色id
	 */
	private int role_id;

	/**
	 * 创建时间
	 */
	private String created_date;

	/**
	 * 创建者id
	 */
	private int creater_id;

	/**
	 * 修改时间
	 */
	private String update_date;

	/**
	 * 权限类型id
	 */
	private int type_id;

	/**
	 * 子类型id
	 */
	private String sub_type_id;

	/**
	 * 子类型中文名称
	 */
	private String sub_type_name;

	/**
	 * 查询权限
	 */
	private int query_permission;

	/**
	 * 新增权限
	 */
	private int add_permission;

	/**
	 * 修改权限
	 */
	private int update_permission;

	/**
	 * 指派权限
	 */
	private int assign_permission;

	/**
	 * 关闭权限
	 */
	private int close_permission;

	/**
	 * 导出权限
	 */
	private int export_permission;

	public String getCreated_date()
	{
		return created_date;
	}

	public void setCreated_date(String created_date)
	{
		this.created_date = created_date;
	}

	public int getCreater_id()
	{
		return creater_id;
	}

	public void setCreater_id(int creater_id)
	{
		this.creater_id = creater_id;
	}

	public String getUpdate_date()
	{
		return update_date;
	}

	public void setUpdate_date(String update_date)
	{
		this.update_date = update_date;
	}

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public int getRole_id()
	{
		return role_id;
	}

	public void setRole_id(int role_id)
	{
		this.role_id = role_id;
	}

	public int getType_id()
	{
		return type_id;
	}

	public void setType_id(int type_id)
	{
		this.type_id = type_id;
	}

	public String getSub_type_id()
	{
		return sub_type_id;
	}

	public void setSub_type_id(String sub_type_id)
	{
		this.sub_type_id = sub_type_id;
	}

	public String getSub_type_name()
	{
		return sub_type_name;
	}

	public void setSub_type_name(String sub_type_name)
	{
		this.sub_type_name = sub_type_name;
	}

	public int getQuery_permission()
	{
		return query_permission;
	}

	public void setQuery_permission(int query_permission)
	{
		this.query_permission = query_permission;
	}

	public int getAdd_permission()
	{
		return add_permission;
	}

	public void setAdd_permission(int add_permission)
	{
		this.add_permission = add_permission;
	}

	public int getUpdate_permission()
	{
		return update_permission;
	}

	public void setUpdate_permission(int update_permission)
	{
		this.update_permission = update_permission;
	}

	public int getAssign_permission()
	{
		return assign_permission;
	}

	public void setAssign_permission(int assign_permission)
	{
		this.assign_permission = assign_permission;
	}

	public int getClose_permission()
	{
		return close_permission;
	}

	public void setClose_permission(int close_permission)
	{
		this.close_permission = close_permission;
	}

	public int getExport_permission()
	{
		return export_permission;
	}

	public void setExport_permission(int export_permission)
	{
		this.export_permission = export_permission;
	}

	/**
	 * 格式化输出
	 */
	@Override
	public String toString()
	{
		return ToStringBuilder.reflectionToString(this, ToStringStyle.SHORT_PREFIX_STYLE);
	}

}
