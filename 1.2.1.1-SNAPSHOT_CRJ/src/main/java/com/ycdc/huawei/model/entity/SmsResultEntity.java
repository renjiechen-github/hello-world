package com.ycdc.huawei.model.entity;

/**
 * @name: SmsResult.java
 * @Description: 
 * @author duanyongrui
 * @since: 2017年3月31日
 */
public class SmsResultEntity { 
	// 短信唯一标识
	private String smsMsgId;
	// 消息发送方的地址，即发送消息的号码。
	private String from;
	// 消息接收方的地址，终端用户用于接收消息的手机号码。
	private String originTo;
	// 短信状态码。如果短信成功已发至运营商，则status为”000000”，且一定会有状态报告;如果短信未成功发送至运营商，status见状态码章节，该消息无状态报告。
	private String status;
	// 短信资源的创建时间，即AEP接收到消费者发送短信请求的时间。格式为：YYYY-MM-DD'T'hh:mm:ss'Z'。
	private String createTime;
	public String getSmsMsgId() {
		return smsMsgId;
	}
	public void setSmsMsgId(String smsMsgId) {
		this.smsMsgId = smsMsgId;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getOriginTo() {
		return originTo;
	}
	public void setOriginTo(String originTo) {
		this.originTo = originTo;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
}
