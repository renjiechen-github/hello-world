package com.ycdc.huawei.model.entity;

/**
 * @name: CallStatusInfo.java
 * @Description: 
 * @author duanyongrui
 * @since: 2017年4月18日
 */
public class Click2CallStatusInfo {
	/*
	 * 事件发生的 unix 时间戳。
	 * 该参数为 UTC 时间，时间格式为“yyyy- MM-dd HH:mm:ss”。
	 */
	private String timestamp;
	// 用户附属信 息，此参数的 值与“点击拨 号场景 API” 或”语音验证 码场景 API“中的 "userData"参数 值一致。
	private String userData;
	// 通话的唯一标 识 ID。
	private String sessionId;
	// 主叫号码，号 码仅支持全局 号码格式，比 如 +8613866887021。
	private String caller;
	// 被叫号码，号 码仅支持全局 号码格式，比 如 +8613866887021。
	private String called;
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getUserData() {
		return userData;
	}
	public void setUserData(String userData) {
		this.userData = userData;
	}
	public String getSessionId() {
		return sessionId;
	}
	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}
	public String getCaller() {
		return caller;
	}
	public void setCaller(String caller) {
		this.caller = caller;
	}
	public String getCalled() {
		return called;
	}
	public void setCalled(String called) {
		this.called = called;
	}
	
}
