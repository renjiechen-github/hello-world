package com.yc.rm.caas.appserver.base.office;

import java.util.List;

import com.yc.rm.caas.appserver.model.base.BaseModel;

/**
 * @author stephen
 * 
 */
public class MailBean extends BaseModel {
	private List<String> recAccount;
	private String title;
	private String filePath;

	public List<String> getRecAccount() {
		return recAccount;
	}

	public void setRecAccount(List<String> recAccount) {
		this.recAccount = recAccount;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
}
