package com.yc.rm.caas.appserver.model.team;

public class TeamLeaders
{
	/** 主键 **/
	int id;
	/** 团队id **/
	int teamId;
	/** 负责人id **/
	int leaderId;
	/** 负责人姓名 **/
	int leaderName;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public int getTeamId()
	{
		return teamId;
	}
	public void setTeamId(int teamId)
	{
		this.teamId = teamId;
	}
	public int getLeaderId()
	{
		return leaderId;
	}
	public void setLeaderId(int leaderId)
	{
		this.leaderId = leaderId;
	}
	public int getLeaderName()
	{
		return leaderName;
	}
	public void setLeaderName(int leaderName)
	{
		this.leaderName = leaderName;
	}
	@Override
	public String toString()
	{
		return "TeamLeaders [id=" + id + ", teamId=" + teamId + ", leaderId=" + leaderId + ", leaderName=" + leaderName + "]";
	}
	
}
