package com.yc.rm.caas.appserver.model.contract;

import java.util.List;

import com.yc.rm.caas.appserver.model.user.User;
import com.yc.rm.caas.appserver.model.syscfg.DictiItem;


/**
 * 合约下拉框
 * @author suntf
 * @date 2016年12月2日
 */
public class AgreementSelect
{
	/**
	 * 管家
	 */
	private List<User> manangerList;
	
	/**
	 * 支付方式
	 */
	private List<DictiItem> payTypeList;
	
	/**
	 * 可租年限
	 */
	private List<DictiItem> rentList;
	
	/**
	 * 装修类型
	 */
	private List<DictiItem> decorateList;
	
	/**
	 * 免租类型
	 */
	private List<DictiItem> freeRentList;
	
	/**
	 * 产权证类型
	 */
	private List<DictiItem> propertyList;

	
	 

	/**
	 * @param manangerList
	 * @param payTypeList
	 * @param rentList
	 * @param decorateList
	 * @param feeRentList
	 * @param propertyList
	 * @date 2017年1月10日
	 */
	public AgreementSelect(List<User> manangerList, List<DictiItem> payTypeList, List<DictiItem> rentList,
			List<DictiItem> decorateList, List<DictiItem> freeRentList, List<DictiItem> propertyList)
	{
		super();
		this.manangerList = manangerList;
		this.payTypeList = payTypeList;
		this.rentList = rentList;
		this.decorateList = decorateList;
		this.freeRentList = freeRentList;
		this.propertyList = propertyList;
	}

	/**
	 * @return the manangerList
	 */
	public List<User> getManangerList()
	{
		return manangerList;
	}

	/**
	 * @param manangerList the manangerList to set
	 */
	public void setManangerList(List<User> manangerList)
	{
		this.manangerList = manangerList;
	}

	/**
	 * @return the payTypeList
	 */
	public List<DictiItem> getPayTypeList()
	{
		return payTypeList;
	}

	/**
	 * @param payTypeList the payTypeList to set
	 */
	public void setPayTypeList(List<DictiItem> payTypeList)
	{
		this.payTypeList = payTypeList;
	}

	/**
	 * @return the rentList
	 */
	public List<DictiItem> getRentList()
	{
		return rentList;
	}

	/**
	 * @param rentList the rentList to set
	 */
	public void setRentList(List<DictiItem> rentList)
	{
		this.rentList = rentList;
	}

	/**
	 * @return the decorateList
	 */
	public List<DictiItem> getDecorateList()
	{
		return decorateList;
	}

	/**
	 * @param decorateList the decorateList to set
	 */
	public void setDecorateList(List<DictiItem> decorateList)
	{
		this.decorateList = decorateList;
	}

	/**
	 * @return the feeRentList
	 */
	public List<DictiItem> getFreeRentList()
	{
		return freeRentList;
	}

	/**
	 * @param feeRentList the feeRentList to set
	 */
	public void setFreeRentList(List<DictiItem> freeRentList)
	{
		this.freeRentList = freeRentList;
	}

	/**
	 * @return the propertyList
	 */
	public List<DictiItem> getPropertyList()
	{
		return propertyList;
	}

	/**
	 * @param propertyList the propertyList to set
	 */
	public void setPropertyList(List<DictiItem> propertyList)
	{
		this.propertyList = propertyList;
	}
	
}
